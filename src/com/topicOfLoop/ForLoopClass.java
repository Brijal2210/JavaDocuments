package com.topicOfLoop;

public class ForLoopClass {
    public static void main(String[] args) {
        //Print 1-10
        for(int i = 1; i<=10; i++){
            System.out.println(i);
        }
        //Print 10-1
        for(int i = 10; i>0; i++){
            System.out.println(i);
        }

    }
}
