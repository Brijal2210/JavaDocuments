package com.topicOfDatatype;

public class DefaultClass {
    static int a;
    static boolean b;
    static char grade;
    static byte b1;
    static short code;
    static long l1;
    static float f1;
    static double d1;

    public static void main(String[] args) {
        System.out.println(" The default value of int " + a);
        System.out.println(" The default value of boolean  " + b);
        System.out.println(" The default value of Character  " + grade);
        System.out.println(" The default value of byte is " + b1);
        System.out.println(" The default value of short is " + code);
        System.out.println("The default value of long is " + l1);
        System.out.println("The default value of float is " + f1);
        System.out.println("The default value of double is " + d1);
    }
}